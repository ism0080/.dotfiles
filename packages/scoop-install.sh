# Scoop packages - Manual installation reference
# Since scoop doesn't have a Brewfile equivalent, install these manually:

# Core tools
scoop install git
scoop install neovim
scoop install pwsh

# CLI utilities
scoop install ripgrep
scoop install fd
scoop install fzf
scoop install jq
scoop install starship

